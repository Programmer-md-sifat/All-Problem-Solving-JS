// Question 2: Write a function maxOfTwo that takes two arguments, a and b, and returns the larger of the two numbers.


function maxofTwo(a, b) {

    return a > b ? a : b;
}
  
console.log(maxofTwo(15, 7)); // Output: 15
console.log(maxofTwo(10, 11)); // Output: 11


